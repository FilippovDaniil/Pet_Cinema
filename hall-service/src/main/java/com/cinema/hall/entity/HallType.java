package com.cinema.hall.entity; // Пакет сущностей hall-service

// Перечисление типов кинозалов.
// Хранится в БД как строка (String) благодаря @Enumerated(EnumType.STRING) в Hall.java.
// Строковое хранение безопаснее порядкового (ORDINAL): при добавлении нового значения
// между существующими порядковые номера сдвигаются и данные в БД становятся некорректными.
public enum HallType {

    NORMAL,  // Стандартный зал — обычные кресла, 2D-экран
    VIP,     // VIP-зал — премиальные кресла, персональное обслуживание, доп.услуги
    THREE_D, // Зал с 3D-оборудованием — спецочки, объёмное изображение
    FIVE_D   // 5D-зал — кресла с вибрацией и эффектами (вода, ветер и т.д.)
}
