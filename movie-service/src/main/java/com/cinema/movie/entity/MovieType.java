package com.cinema.movie.entity;

// Тип формата фильма — влияет на то, в каком зале можно показывать сеанс.
// Значения хранятся в БД как строки (через @Enumerated(EnumType.STRING) в Movie.java),
// а не как числа — так данные читаемы напрямую в таблице и устойчивы к изменению порядка.
public enum MovieType {
    TWO_D,   // Стандартный плоский формат — подходит для любого зала
    THREE_D, // Стереоскопическое 3D — нужен зал типа THREE_D
    FIVE_D   // Полное погружение (вибрация, ветер, вода) — только зал типа FIVE_D
}
