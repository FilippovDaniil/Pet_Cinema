package com.cinema.order.entity;

// Тип заказа — определяет, что именно купил пользователь.
// Используется для фильтрации заказов и логики обработки (только TICKET требует бронирования места).
public enum OrderType {
    TICKET,  // Только билет(ы) на сеанс
    FOOD,    // Только еда из меню (без билета)
    MIXED    // Билет + еда в одном заказе
}
