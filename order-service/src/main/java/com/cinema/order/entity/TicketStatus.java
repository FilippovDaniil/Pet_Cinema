package com.cinema.order.entity;

// Статус билета.
// Билет создаётся сразу в ACTIVE после оплаты заказа.
// USED — пробит на входе в зал (в данном учебном проекте смена статуса не реализована).
// CANCELLED — соответствует отменённому заказу (Order.status = CANCELLED).
public enum TicketStatus {
    ACTIVE,    // Действующий билет, готов к использованию
    USED,      // Билет использован (пользователь прошёл в зал)
    CANCELLED  // Билет аннулирован (возврат/отмена заказа)
}
